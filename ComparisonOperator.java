public class ComparisonOperator {
    public static void main(String[] args) {
        // Comparison Operator
        int x = 1;
        int y = 1;
        // = assignment operator
        // == equality operator
        // != inequality operator
        // >, <, >=, <=

        // boolean expression TRUE/FALSE
        System.out.println(x == y);
        System.out.println(x != y);
        System.out.println(x <= y);
        System.out.println(x >= y);
    }
}
