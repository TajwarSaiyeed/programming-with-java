public class Primitive_Types {
    public static void main(String[] args) {
        // 1 bytes
        byte age = 20;
        // 4 bytes
        int viewsCount = 123_456_789;
        // 8 bytes
        long long_viewsCount = 3_123_456_789L;
        // decimal
        float price_float = 10.99F;
        // 8 bytes
        double price_double = 10.99;
        // 2 bytes
        char letter = 'A';
        // 1 bytes
        boolean isEligible = false;
    }
}
